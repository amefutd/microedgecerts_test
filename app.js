// Web3のインスタンスを待ってからコントラクトをインスタンス化

document.addEventListener('DOMContentLoaded', async () => {
    if (window.ethereum) {
        // MetaMaskのアカウントにアクセス
        await window.ethereum.request({ method: 'eth_requestAccounts' });
        
        // Web3のインスタンスを作成
        const web3 = new Web3(window.ethereum);
        const contract = new web3.eth.Contract(contractABI, contractAddress);

        // var contract = new window.web3.eth.Contract(contractABI).at(contractAddress);
        document.getElementById('issuerForm').addEventListener('submit', async (e) => {
            e.preventDefault();
            const recipientAddress = document.getElementById('recipientAddress').value;
            const tokenURI = document.getElementById('tokenURI').value;

            // IPFSへのアップロードとHashの取得
            const ipfsHash = await uploadToIPFS(tokenURI);

            const accounts = await window.web3.eth.getAccounts();
            const account = accounts[0];
            try {
                await contract.methods.mint(recipientAddress, ipfsHash).send({ from: account });
                document.getElementById('issuerMessage').textContent = 'Certificate issued successfully!';
            } catch (error) {
                document.getElementById('issuerMessage').textContent = 'Error issuing certificate. Check console for details.';
                console.error(error);
            }
        });

        document.getElementById('holderForm').addEventListener('submit', async (e) => {
            e.preventDefault();
            const holderAddress = document.getElementById('holderAddress').value;
            const certificatesDiv = document.getElementById('holderCertificates');
            certificatesDiv.innerHTML = '';
            try {
                const totalSupply = await contract.methods.totalSupply().call();
                for (let i = 0; i < totalSupply; i++) {
                    const tokenId = await contract.methods.tokenByIndex(i).call();
                    const owner = await contract.methods.ownerOf(tokenId).call();
                    if (owner.toLowerCase() === holderAddress.toLowerCase()) {
                        const tokenURI = await contract.methods.tokenURI(tokenId).call();
                        const certificateDiv = document.createElement('div');
                        certificateDiv.className = 'certificate';
                        certificateDiv.textContent = `Token ID: ${tokenId}, Token URI: ${tokenURI}`;
                        certificatesDiv.appendChild(certificateDiv);
                    }
                }
                document.getElementById('holderMessage').textContent = '';
            } catch (error) {
                document.getElementById('holderMessage').textContent = 'Error fetching certificates. Check console for details.';
                console.error(error);
            }
        });

        document.getElementById('verifierForm').addEventListener('submit', async (e) => {
            e.preventDefault();
            const tokenId = document.getElementById('tokenId').value;
            const resultDiv = document.getElementById('verifierResult');
            resultDiv.innerHTML = '';
            try {
                const owner = await contract.methods.ownerOf(tokenId).call();
                const tokenURI = await contract.methods.tokenURI(tokenId).call();
                const certificateDiv = document.createElement('div');
                certificateDiv.className = 'certificate';
                certificateDiv.textContent = `Token ID: ${tokenId}, Owner: ${owner}, Token URI: ${tokenURI}`;
                resultDiv.appendChild(certificateDiv);
                document.getElementById('verifierMessage').textContent = '';
            } catch (error) {
                document.getElementById('verifierMessage').textContent = 'Error verifying certificate. Check console for details.';
                console.error(error);
            }
        });
    } else {
        console.error("Web3 not initialized. Please install MetaMask.");
    }
});

async function uploadToIPFS(data) {
    const response = await fetch('https://ipfs.infura.io:5001/api/v0/add', {
        method: 'POST',
        body: data
    });
    const result = await response.json();
    await fetch(`https://ipfs.infura.io:5001/api/v0/pin/add?arg=${result.Hash}`);  // Pin the data to ensure persistence
    return result.Hash;
}
